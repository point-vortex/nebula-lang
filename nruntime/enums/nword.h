#ifndef NEBULA_LANG_NWORD_H
#define NEBULA_LANG_NWORD_H

#include <stdint.h>

typedef int32_t nword;

const nword I_CODE_MASK = 0b00000000000000000000000011111111;
const nword I_TYPE_MASK = 0b00000000000000000000111000000000;

const nword I_HALT      = 0b00000000000000000000000000000000;
const nword I_CONST     = 0b00000000000000000000000000000001;
const nword I_PUSH      = 0b00000000000000000000000000000010;
const nword I_POP       = 0b00000000000000000000000000000011;

const nword I_ADD       = 0b00000000000000000000000000000100;
const nword I_SUB       = 0b00000000000000000000000000000101;
const nword I_MUL       = 0b00000000000000000000000000000110;
const nword I_DIV       = 0b00000000000000000000000000000111;

const nword I_AND       = 0b00000000000000000000000000001000;
const nword I_OR        = 0b00000000000000000000000000001001;
const nword I_XOR       = 0b00000000000000000000000000001010;
const nword I_NOT       = 0b00000000000000000000000000001011;
const nword I_SHL       = 0b00000000000000000000000000001100;
const nword I_SHR       = 0b00000000000000000000000000001101;

const nword I_JMP       = 0b00000000000000000000000000001110;
const nword I_JMPZ   = 0b00000000000000000000000000001111;

const nword I_EQUAL     = 0b00000000000000000000000000010000;
const nword I_NEQUAL    = 0b00000000000000000000000000010001;
const nword I_GREATER   = 0b00000000000000000000000000010010;
const nword I_GREQUAL   = 0b00000000000000000000000000010011;
const nword I_LESS      = 0b00000000000000000000000000010100;
const nword I_LSEQUAL   = 0b00000000000000000000000000010101;
const nword I_LAND      = 0b00000000000000000000000000010110;
const nword I_LOR       = 0b00000000000000000000000000010111;
const nword I_LNOT      = 0b00000000000000000000000000011000;

const nword I_CALL      = 0b00000000000000000000000000011001;
const nword I_RET       = 0b00000000000000000000000000011010;
const nword I_SYSCALL   = 0b00000000000000000000000000011011;
const nword I_RESERVED  = 0b00000000000000000000000000011100;

const nword I_LOAD      = 0b00000000000000000000000000011101;
const nword I_STORE     = 0b00000000000000000000000000011110;
const nword I_ARG       = 0b00000000000000000000000000011111;

const nword MAX_MEMORY = 65536;

#endif //NEBULA_LANG_NWORD_H
